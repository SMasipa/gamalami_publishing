<h2>What is Gamalami?</h2>
<hr>
Gamalami is an online platform that makes it easier for writers to sell and promote literature work.We provide publications which have been written in various languages.

<h3>Our Aim</h3>

Our aim is to make self publication easier for professional and aspiring writers alike.Our platform was designed with the following factors in mind; <strong>Simplicity, High Quality and Accessibility</strong>.</br>

We strive to find and implement effective strategies that will help us achieve our goal of connecting readers with high quality literature work, and writers with a loyal readership.
